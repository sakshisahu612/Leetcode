//User function template for C++
class Solution{
public:	
	
	
	int isPalindrome(string S)
	{
	    string ans = "";
	    for(int i = S.length()-1; i>=0; i--){
	        ans += S[i];
	    }
	    if(S==ans){
	        return 1;
	    }
	    
	    return 0;
	}

};

